function escapeHtml(value) {
  return String(value).replace(/[&<>"']/g, ch => ({
    "&":"&amp;", "<":"&lt;", ">":"&gt;", '"':"&quot;", "'":"&#039;"
  }[ch]));
}
function excerpt(article, length = 85) {
  const text = article.paragraphs.join("").replace(/\s+/g, " ");
  return text.length > length ? text.slice(0, length) + "……" : text;
}
